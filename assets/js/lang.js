const typedStrings = {
    en: ["Web Developer", "AI Enthusiast"],
    uz: ["Veb Dasturchi", "AI Qiziquvchisi"]
};

let typedInstance;

function updateTypedText(lang) {
    const typedElement = document.querySelector('.typed');
    if (!typedElement) return;

    if (typedInstance) typedInstance.destroy();

    typedInstance = new Typed('.typed', {
        strings: typedStrings[lang],
        typeSpeed: 100,
        backSpeed: 50,
        backDelay: 2000,
        loop: true
    });
}

function setLanguage(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) el.innerText = translations[lang][key];
    });
    updateTypedText(lang);
    localStorage.setItem('lang', lang);
}

window.onload = () => {
    const saved = localStorage.getItem('lang') || 'en';
    setLanguage(saved);
    const btn = document.getElementById('lang_' + saved);
    if (btn) btn.classList.add('active');
};

document.getElementById('lang_en').addEventListener('click', () => setLanguage('en'));
document.getElementById('lang_uz').addEventListener('click', () => setLanguage('uz'));
